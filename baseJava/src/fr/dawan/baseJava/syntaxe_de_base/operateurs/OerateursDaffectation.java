package fr.dawan.baseJava.syntaxe_de_base.operateurs;

public class OerateursDaffectation {

	public static void main(String[] args) {

		int a = 10;

		// Avec les operateurs d'affectation
		a += 5; // a = a + 5;
		a -= 5; // a = a - 5;
		a *= 5; // a = a * 5;
		a /= 5; // a = a / 5;

	}

}
