package fr.dawan.baseJava.syntaxe_de_base.operateurs;

public class OperateursDincrementationEtDecrementation {

	public static void main(String[] args) {
		
		int c = 10;
		
		c++;  //c =  c + 1;  //11
		System.out.println(c);
		
		int d = 15; 
		d--;  //d = d- 1
		System.out.println(d);

	}

}
